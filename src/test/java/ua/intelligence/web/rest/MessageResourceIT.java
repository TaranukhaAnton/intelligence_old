    @Test
    @Transactional
    void putExistingMessage() throws Exception {
        // Initialize the database
        messageRepository.saveAndFlush(message);

        int databaseSizeBeforeUpdate = messageRepository.findAll().size();

        // Update the message
        Message updatedMessage = messageRepository.findById(message.getId()).get();
        // Disconnect from session so that the updates on updatedMessage are not directly saved in db
        em.detach(updatedMessage);
        updatedMessage
            .date(UPDATED_DATE)
            .frequency(UPDATED_FREQUENCY)
            .senderCallSign(UPDATED_SENDER_CALL_SIGN)
            .receiverCallSign(UPDATED_RECIEVER_CALL_SIGN)
            .text(UPDATED_TEXT)
            .sourceUuid(UPDATED_SOURCE_UUID);

        restMessageMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedMessage.getId())
                    .with(csrf())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedMessage))
            )
            .andExpect(status().isOk());

        // Validate the Message in the database
        List<Message> messageList = messageRepository.findAll();
        assertThat(messageList).hasSize(databaseSizeBeforeUpdate);
        Message testMessage = messageList.get(messageList.size() - 1);
        assertThat(testMessage.getDate()).isEqualTo(UPDATED_DATE);
        assertThat(testMessage.getFrequency()).isEqualTo(UPDATED_FREQUENCY);
        assertThat(testMessage.getSenderCallSign()).isEqualTo(UPDATED_SENDER_CALL_SIGN);
        assertThat(testMessage.getReceiverCallSign()).isEqualTo(UPDATED_RECIEVER_CALL_SIGN);
        assertThat(testMessage.getText()).isEqualTo(UPDATED_TEXT);
        assertThat(testMessage.getSourceUuid()).isEqualTo(UPDATED_SOURCE_UUID);
    }

