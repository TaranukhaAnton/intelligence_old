    private static final ZonedDateTime DEFAULT_DATE = ZonedDateTime.ofInstant(Instant.ofEpochMilli(0L), ZoneOffset.UTC);
    private static final ZonedDateTime UPDATED_DATE = ZonedDateTime.now(ZoneId.systemDefault()).withNano(0);

    private static final String DEFAULT_FREQUENCY = "AAAAAAAAAA";
    private static final String UPDATED_FREQUENCY = "BBBBBBBBBB";

    private static final String DEFAULT_SENDER_CALL_SIGN = "AAAAAAAAAA";
    private static final String UPDATED_SENDER_CALL_SIGN = "BBBBBBBBBB";

    private static final String DEFAULT_RECIEVER_CALL_SIGN = "AAAAAAAAAA";
    private static final String UPDATED_RECIEVER_CALL_SIGN = "BBBBBBBBBB";

    private static final String DEFAULT_TEXT = "AAAAAAAAAA";
    private static final String UPDATED_TEXT = "BBBBBBBBBB";

    private static final String DEFAULT_SOURCE_UUID = "AAAAAAAAAA";
    private static final String UPDATED_SOURCE_UUID = "BBBBBBBBBB";

    private static final String ENTITY_API_URL = "/api/messages";
    private static final String ENTITY_API_URL_ID = ENTITY_API_URL + "/{id}";

    private static Random random = new Random();
    private static AtomicLong count = new AtomicLong(random.nextInt() + (2 * Integer.MAX_VALUE));

    @Autowired
    private MessageRepository messageRepository;

    @Autowired
    private EntityManager em;

    @Autowired
    private MockMvc restMessageMockMvc;

    private Message message;

    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Message createEntity(EntityManager em) {
        Message message = new Message()
            .date(DEFAULT_DATE)
            .frequency(DEFAULT_FREQUENCY)
            .senderCallSign(DEFAULT_SENDER_CALL_SIGN)
            .receiverCallSign(DEFAULT_RECIEVER_CALL_SIGN)
            .text(DEFAULT_TEXT)
            .sourceUuid(DEFAULT_SOURCE_UUID);
        return message;
    }

    /**
     * Create an updated entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
